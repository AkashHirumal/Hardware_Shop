    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            Customer customer = CustomerManagementController.searchByName(txtSearchCustomer.getText());
            if (customer != null) {
                txtCustomerName.setText(customer.getName());
                txtCustomerContact.setText(String.valueOf(customer.getContactInfo()));
            } else {
                JOptionPane.showMessageDialog(null, "Customer Not Found");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CustomerManagementView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

