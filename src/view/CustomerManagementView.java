    private void loadtable() {
        try {
            ArrayList<Customer> customersList = CustomerManagementController.getAllCustomers();
            DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
            dtm.setRowCount(0);
            int id = 1;
            for (Customer customer : customersList) {
                Object[] rowData = {id, customer.getName(), customer.getContactInfo()};
                dtm.addRow(rowData);
                id++;
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CustomerManagementView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

