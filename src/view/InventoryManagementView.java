    private void loadtable() {
        try {
            ArrayList<Item> itemsList = InventoryManagementController.getAllItems();
            DefaultTableModel dtm = (DefaultTableModel) tblInventories.getModel();
            dtm.setRowCount(0);
            int id = 1;
            for (Item item : itemsList) {
                Object[] rowData = {id, item.getItemName(), item.getItemDesc(), item.getItemQty()};
                dtm.addRow(rowData);
                id++;
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(InventoryManagementView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

