    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            Item item = InventoryManagementController.searchByName(txtSearchItem.getText());
            if (item != null) {
                txtItemName.setText(item.getItemName());
                txtItemDesc.setText(item.getItemDesc());
                txtItemQty.setText(String.valueOf(item.getItemQty()));
                txtItemPrice.setText(String.valueOf(item.getItemPrice()));
            } else {
                JOptionPane.showMessageDialog(null, "Item Not Found");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(InventoryManagementView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

